    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pat", "root", "pat@admin#0987");
        String username = edtUsername.getText();
        String password = edtPassword.getText();
        String sql = "SELECT * FROM signup WHERE username=? AND password=?";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setString(1, username);
        stmt.setString(2, password);
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            UserSession.loggedInUsername = username; // Store the username in UserSession
            JOptionPane.showMessageDialog(this, "Login Successfully");

            // Insert the username into asseen table
            String insertQuery = "INSERT INTO asseen (name) VALUES (?)";
            try (PreparedStatement insertPst = con.prepareStatement(insertQuery)) {
                insertPst.setString(1, UserSession.loggedInUsername);
                insertPst.executeUpdate();
                System.out.println("Username successfully inserted into asseen.");
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error inserting username: " + e.getMessage());
            }

            // Proceed to homepage
            homepage hp = new homepage();
            hp.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Username or password is wrong");
            edtUsername.setText("");
            edtPassword.setText("");
        }
        con.close();
    } catch (Exception e) {
        System.out.println(e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

