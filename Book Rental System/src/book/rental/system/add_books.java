    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        String num = txtsr.getText();
        String name = txtname.getText();
        String genres = txtgenres.getText();
        String author = txtauthor.getText();
        String rent = txtrent.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/book_rental_system" , "root" , "123456");
            insert = con.prepareStatement("INSERT INTO admin_book(sr,name,genres,author,rent_amt,image) VALUES(?,?,?,?,?,?)");
            insert.setString(1, num);
            insert.setString(2, name);
            insert.setString(3, genres);
            insert.setString(4, author);
            insert.setString(5, rent);
            InputStream is = new FileInputStream(new File(path2));
            insert.setBlob(6, is);
           
            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Book Added Successfully");
            
                txtsr.setText("");
                txtname.setText("");
                txtgenres.setText("");
                txtauthor.setText("");
                txtrent.setText("");
                photo.setIcon(null);
                txtsr.requestFocus();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(add_books.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
        
         catch (SQLException ex) {
            Logger.getLogger(add_books.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(add_books.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnaddActionPerformed

