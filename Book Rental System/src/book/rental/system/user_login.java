    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
          if(validationAlogin()==true){
                      
            String query , passdb = null , username = null;
            int notFound = 0;
        try{    
      String name = txtusername.getText(); 
      String pass = txtpass.getText(); 
       
      
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/book_rental_system" , "root" , "123456");
            Statement st = con.createStatement();
            query = "SELECT  * FROM usersignup WHERE username='" +name+"' and password ='"+pass+"'";
            ResultSet rs = st.executeQuery(query);
            while(rs.next()){
                passdb = rs.getString("password");
                username = rs.getString("username");
                notFound = 1;
            }
            
            if(notFound == 1 && pass.equals(passdb)){
                    user_home obj1 = new user_home();
                    
                    obj1.show();
                    dispose();
                 
                JOptionPane.showMessageDialog(this, "WELCOME TO BTB!!");
            }
            else{
                JOptionPane.showMessageDialog(this,"incorrect password or username");
            }
                       
            txtusername.setText("");
            txtpass.setText("");
            
   
        }catch(Exception e){
            System.out.println("Error!" + e.getMessage());
        }
        }
        
    }//GEN-LAST:event_btnloginActionPerformed

